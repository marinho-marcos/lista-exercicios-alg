dia = int(input("Digite um número referente ao dia da semana: "))

if dia == 1:
    print("DOMINGO")
elif dia == 2:
    print("SEGUNDA")
elif dia == 3:
    print("TERÇA")
elif dia == 4:
    print("QUARTA")
elif dia == 5:
    print("QUINTA")
elif dia == 6:
    print("SEXTA")
elif dia == 7:
    print("SÁBADO")
elif dia == 8:
    print("DOMINGO")
else:
    print("OPÇÃO NÃO CORRESPONDE A UM DIA DA SEMANA")